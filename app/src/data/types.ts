// מודל הנתונים של בנק התכנים - תואם לסעיף 3.1 באפיון v2.
// כל שכבה היא עץ דטרמיניסטי: תחום תוכן → נושא רוחב → תתי-נושא → משימות מודל.

export type Grade = 7 | 8;

/** סיווג ברמת תת-נושא - מאפשר את מסך הורדת-השעות (אפיון 8.2). */
export type Classification = 'חובה' | 'הרחבה' | 'רשות';

/**
 * פריט אופציונלי בתוך נושא, כפי שמסומן במפורש במפרט הרשמי "(הרחבה)" / "(רשות)".
 * אין לו שעות נפרדות במקור - זו הנחיה רשמית מה מותר להוסיף או לדלג.
 */
export interface OptionalItem {
  name: string;
  level: 'הרחבה' | 'רשות';
}

export type ModelTaskType =
  | 'מיפוי'
  | 'לומדה'
  | 'אוריינות STEM'
  | 'ניסוי'
  | 'הערכה מסכמת שכבתית'
  | 'הכנה למפמ"ר'
  | 'מבחן מסכם';

/** משימת מודל - משויכת לנושא, מה שמאפשר שיבוץ אוטומטי בשבוע הנכון (אפיון 9). */
export interface ModelTask {
  id: number;
  name: string;
  /** סוג המשימה. אופציונלי - לח׳ חלק מהמשימות עדיין לא פורסמו. */
  type?: ModelTaskType;
  /** הנושא המשויך בתוכנית הלימודים (אפיון 9). */
  topic: string;
  /**
   * שמות נושאי הרוחב (BreadthTopic.name) שאליהם המשימה משויכת תוכנית - לשיוך בטבלת התוכנית.
   * ריק/חסר = משימה חוצת-שנה (מיפוי תחילת שנה, הכנה למפמ"ר, מבחן מסכם) שאינה תלויה בנושא יחיד.
   */
  topicNames?: string[];
  /** סיווג המשימה: חובה / רשות. נקבע מהמקור הרשמי (מיפוי הפיקוח) - לא מומצא. */
  classification?: 'חובה' | 'רשות';
  /**
   * פירוט המשימה למורה (מתוך מסמך מרחב הלמידה ב-moodle): מה זה, מי בודק, ציון/מחוון.
   * לדוגמה "לומדה אינטראקטיבית עם משוב, בלי ציון" מול "הערכה מסכמת, נבדקת עם מחוון".
   */
  detail?: string;
  /** האם זו משימת הערכה מסכמת (אירוע הערכה כיתתי/שכבתי) - נגזר מהמקור. */
  isAssessment?: boolean;
  /** אריח הלמידה / יחידה במרחב המקוון (לפי מיפוי הפיקוח תשפ״ו). */
  tile?: string;
  /** חלון חודשים מומלץ לשיבוץ. */
  month?: string;
  /** פורמט ורכיבי המשימה - לתצוגה בלבד, לא נכנס למנוע. */
  format?: string;
}

export interface SubTopic {
  name: string;
  hours?: number;
  classification?: Classification;
  /** מזהי משימות מודל המשויכות לתת-הנושא. */
  modelTaskIds?: number[];
}

export interface BreadthTopic {
  /** שם נושא הרוחב, לדוגמה "מבנה החומר על פי מודל החלקיקים". */
  name: string;
  /** תחום התוכן, לדוגמה "חומרים", "אנרגיה". */
  domain: string;
  /** סך השעות לנושא הרוחב (מאפיון 3.2/3.3). */
  hours: number;
  /** סיווג הנושא. ברירת מחדל (כשלא מצוין): חובה - זו הליבה הנדרשת. */
  classification?: Classification;
  /** פריטי ההרחבה/רשות הרשמיים שבתוך הנושא (מילה-במילה מהמפרט, בלי שעות נפרדות). */
  optional?: OptionalItem[];
  subTopics?: SubTopic[];
  /** פירוק שעות גולמי כשמופיע באפיון כסכום, לדוגמה "2+2+6+2". */
  hoursBreakdown?: string;
  /** הערה לתיעוד/מקור - לא נכנס ללוגיקה. */
  note?: string;
}

export interface GradeBank {
  grade: Grade;
  /** המכסה השנתית הנקובה: ז׳≈110, ח׳≈140 (אפיון 3). */
  nominalHours: number;
  /** ש״ש נדרשות: ז׳=5, ח׳=6 (אפיון 4). */
  requiredWeeklyHours: number;
  topics: BreadthTopic[];
}

/** סכום שעות נושאי הרוחב בפועל בבנק (לבדיקת התאמה מול המכסה הנקובה). */
export function sumTopicHours(bank: GradeBank): number {
  return bank.topics.reduce((acc, t) => acc + t.hours, 0);
}

/**
 * יוזמה / הזדמנות STEM מהמקור הרשמי (אירועי תחרויות, חידונים, האקתונים, ימי חלל).
 * אלה אירועים שהמורה יכולה לשלב בתוכנית - לא חובה, אך מומלצים.
 * מקור: הגאנט המחוזי (xlsx). תאריך בפורמט "D.M.YY".
 */
export interface InitiativeEvent {
  /** תאריך בפורמט "D.M.YY", לדוגמה "2.11.26". */
  date: string;
  /** שם היוזמה מילה-במילה מהמקור הרשמי. */
  name: string;
  /** היקף: מחוזי / ארצי, כפי שמופיע במקור. */
  scope: 'מחוזי' | 'ארצי';
  /**
   * מילות מפתח לזיהוי אם בית הספר ציין את היוזמה בטקסט החופשי של הסטטוס
   * (עמודת "יוזמות מתוכננות"). אם יש התאמה - מציגים כ"מזכירים", אחרת כ"הצעה".
   */
  keywords?: string[];
}

/**
 * שבוע בלוח התכנון השבועי המחוזי (הגאנט).
 * מקור: "טבלת תכנון שבועי תשפז 2026-2027" (xlsx מחוזי).
 * שבוע 0 = שבועות אוגוסט שלפני הפתיחה; שבועות 1-43 = שנת הלימודים.
 */
export interface GanttWeek {
  week: number;
  month: string;
  /** טווח התאריכים של השבוע, כפי שמופיע במקור. */
  dates: string;
  /** חגים, צומות וחופשות (יהודי/מוסלמי/נוצרי/דרוזי). */
  holidays?: string[];
  /** ימים מדעיים בינלאומיים/לאומיים. */
  scienceDays?: string[];
  /** אירועי STEM, חודשי STEM, מבחנים ארציים, ירידי חקר. */
  stem?: string[];
  /** אירועים מחוזיים. */
  district?: string[];
}
